import esbuild from "esbuild";
import process from "process";
import builtins from "builtin-modules";

const banner =
`/*
THIS IS A GENERATED/BUNDLED FILE BY ESBUILD
if you want to view the source, please visit the github repository of this plugin
*/
`;

const prod = (process.argv[2] === "production");

const context = await esbuild.context({
	banner: {
		js: banner,
	},
	entryPoints: ["src/main.ts"],
	bundle: true,
	external: [
		"obsidian",
		"electron",
		"@codemirror/autocomplete",
		"@codemirror/collab",
		"@codemirror/commands",
		"@codemirror/language",
		"@codemirror/lint",
		"@codemirror/search",
		"@codemirror/state",
		"@codemirror/view",
		"@lezer/common",
		"@lezer/highlight",
		"@lezer/lr",
		...builtins],
	format: "cjs",
	target: "es2018",
	logLevel: "info",
	sourcemap: prod ? false : "inline",
	treeShaking: true,
	outfile: "main.js",
});

const pluginDir = "2html-dev-vault/.obsidian/plugins/obsidian-2html-plugin";

import fs from "fs";
function copyToDevVault() {
	if (!fs.existsSync(pluginDir)) {
		fs.mkdirSync(pluginDir, { recursive: true });
	}
	fs.copyFileSync("main.js", `${pluginDir}/main.js`);
	fs.copyFileSync("manifest.json", `${pluginDir}/manifest.json`);
	console.log("Copied build files to dev vault plugin directory");
}

if (prod) {
	await context.rebuild();
	copyToDevVault();
	process.exit(0);
} else {
	// For watch mode, we'd ideally use a plugin, but mapping outfile directly works too.
	// Since outfile is main.js, we'll just copy once initially
	copyToDevVault();
	await context.watch();
}
